#include <stdio.h>

int main() {
    // Abrir um arquivo para escrita
    FILE *arquivo = fopen("exemplo.txt", "w");

    // Verificar se o arquivo foi aberto com sucesso
    if (arquivo == NULL) {
        printf("Erro ao abrir o arquivo.\n");
        return 1; // Sair do programa com código de erro
    }

    // Escrever no arquivo
    fprintf(arquivo, "Olá, este é um arquivo de exemplo!\n");

    // Fechar o arquivo
    fclose(arquivo);

    printf("Arquivo criado com sucesso.\n");

    return 0; // Sair do programa sem erros
}#include <stdio.h>

